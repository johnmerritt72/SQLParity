    [Fact]
    public void NewTable_WrappedWithObjectIdGuardNotDrop()
    {
        using var t = new TempFolder();
        var change = ChangeFor(ObjectType.Table, "dbo", "Orders", ChangeStatus.New,
            ddlA: "CREATE TABLE [dbo].[Orders] (Id INT NOT NULL)");

        new FolderSyncWriter().WriteChanges(new[] { change }, ContextFor(t.Path),
            "srv", "MyDb", FixedNow);

        string content = t.Read("TABLE.dbo.Orders.sql");
        Assert.Contains("IF OBJECT_ID(N'[dbo].[Orders]', N'U') IS NULL", content);
        Assert.DoesNotContain("DROP TABLE", content);
    }

    [Fact]
    public void Manifest_RecordsCorrectCategoriesPerChange()
    {
        using var t = new TempFolder();
        var existing = t.WriteFile("PROC.dbo.Mod.sql", "-- old");
        var multi = t.WriteFile("multi.sql", "CREATE PROC dbo.M1 AS SELECT 1\nGO\n");

        var modId = SchemaQualifiedName.TopLevel("dbo", "Mod");
        var m1Id = SchemaQualifiedName.TopLevel("dbo", "M1");
        var ctx = ContextFor(t.Path,
            (modId, existing, Single: true),
            (m1Id, multi, Single: false));

        var changes = new[]
        {
            new Change
            {
                Id = modId, ObjectType = ObjectType.StoredProcedure,
                Status = ChangeStatus.Modified,
                DdlSideA = "CREATE PROC dbo.Mod AS SELECT 999",
                DdlSideB = "-- old",
                ColumnChanges = Array.Empty<ColumnChange>(),
            },
            new Change
            {
                Id = m1Id, ObjectType = ObjectType.StoredProcedure,
                Status = ChangeStatus.Modified,
                DdlSideA = "CREATE PROC dbo.M1 AS SELECT 999",
                DdlSideB = "CREATE PROC dbo.M1 AS SELECT 1",
                ColumnChanges = Array.Empty<ColumnChange>(),
            },
            new Change
            {
                Id = SchemaQualifiedName.TopLevel("dbo", "BrandNew"),
                ObjectType = ObjectType.StoredProcedure,
                Status = ChangeStatus.New,
                DdlSideA = "CREATE PROC dbo.BrandNew AS SELECT 1",
                DdlSideB = null,
                ColumnChanges = Array.Empty<ColumnChange>(),
            },
        };

        var manifest = new FolderSyncWriter().WriteChanges(changes, ctx, "srv", "MyDb", FixedNow);

        Assert.Single(manifest.FilesUpdated);     // PROC.dbo.Mod.sql overwritten in place
        Assert.Single(manifest.FilesCommentedOut); // multi.sql commented out
        // Created: M1 (extracted from multi-file) + BrandNew = 2.
        Assert.Equal(2, manifest.FilesCreated.Count);
        Assert.Empty(manifest.Errors);
    }

    [Fact]
    public void ModifiedChange_WithoutSourceFileMapping_IsSkipped()
    {
        using var t = new TempFolder();
        // No mapping in context — change should be skipped, not crash.
        var change = ChangeFor(ObjectType.StoredProcedure, "dbo", "Ghost",
            ChangeStatus.Modified, ddlA: "CREATE PROC dbo.Ghost AS SELECT 1");

        var manifest = new FolderSyncWriter().WriteChanges(
            new[] { change }, ContextFor(t.Path), "srv", "MyDb", FixedNow);

        var skip = Assert.Single(manifest.Skipped);
        Assert.Contains("Ghost", skip);
        Assert.Empty(manifest.Errors);
        Assert.Empty(manifest.FilesUpdated);
    }
}
